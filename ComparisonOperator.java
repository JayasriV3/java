package practice;

public class ComparisonOperator {
public static void main(String[] arg) {
	int count1 = 4;
	int count2 = 5;
	
	if (count1 == count2) {
	System.out.println("count1 is equal to count2");
	}
	
	if (count1 != count2) {
	System.out.println("count1 is not equal to count2");
	}
	
	if (count1 > count2) {
	System.out.println("count1 is greater than count2");
	}
	
	if (count1 >= count2) {
	System.out.println("count1 is greater than equal to count2");
	}
	
	if (count1 < count2) {
	System.out.println("count1 is lesser than count2");
	}
	
	if (count1 <= count2) {
	System.out.println("count1 is lesser than equal to count2 ");
	}
}
}
