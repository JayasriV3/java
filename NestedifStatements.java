package practice;

public class NestedifStatements {
public static void main(String[] args) {
	int Marks = 35;
	
	if (Marks < 50) {
		System.out.println("The mark is lesser than 50");
	}
	if (Marks < 40) {
		System.out.println("The mark is lesser than 40");
	}
	if (Marks < 36) {
		System.out.println("The mark is lesser than 36");
	}
}
}
