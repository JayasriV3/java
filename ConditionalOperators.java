package practice;

public class ConditionalOperators {
public static void main(String[] arg) {
	int res1 = 12;
	int res2 = 13;
	
	if ((res1 == 12)&&(res2 == 13)){
		System.out.println("and statement is correct");
	}
	
	if ((res1 == 2)||(res2 == 1)){
		System.out.println("or statement is correct");
	}
}
}
